$totalEarnings = 0;

// Check if the first query returned a result
if ($result1->num_rows > 0) {
    // Fetch the total earnings from booking_payments
    $row1 = $result1->fetch_assoc();
    $totalEarnings = $row1['total_earnings'];
} 

// Query to get the revenue from the payments table
$sql2 = "SELECT p.id, p.order_id, p.package_id, p.package_name, SUM(pa.attraction_cost) AS total_revenue
        FROM payments p
        JOIN package_attractions pa ON p.package_id = pa.package_id
        GROUP BY p.id, p.order_id, p.package_id, p.package_name";

// Execute the second query
$result2 = $conn->query($sql2);

// Check if the second query returned results
if ($result2->num_rows > 0) {
    // Loop through each payment and add to the total earnings
    while($row2 = $result2->fetch_assoc()) {
        // Add the revenue from the current payment to total earnings
        $totalEarnings += $row2['total_revenue'];
