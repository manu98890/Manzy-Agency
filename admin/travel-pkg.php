if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Sanitize user input
    $package_name = $conn->real_escape_string($_POST['package_name']);
    $total_cost = $conn->real_escape_string($_POST['total_cost']);
    $duration = $conn->real_escape_string($_POST['duration']);
    $weather = $conn->real_escape_string($_POST['weather']);
    


    if (isset($_FILES['fileToUpload']) && $_FILES['fileToUpload']['error'] == 0) {
        $target_dir = "../res/img/dashboard/places/";
        $target_file = $target_dir . basename($_FILES["fileToUpload"]["name"]);
        $uploadOk = 1;
        $imageFileType = strtolower(pathinfo($target_file, PATHINFO_EXTENSION));

        // Check if image file is a real image or fake image
        $check = getimagesize($_FILES["fileToUpload"]["tmp_name"]);
        if ($check !== false) {
            echo "File is an image - " . $check["mime"] . ".";
            $uploadOk = 1;
        } else {
            echo "File is not an image.";
            $uploadOk = 0;
        }

        // Move the file if it's valid
        if ($uploadOk && move_uploaded_file($_FILES["fileToUpload"]["tmp_name"], $target_file)) {


            $image = basename($_FILES["fileToUpload"]["name"]);

            $insertQuery = "INSERT INTO travel_packages (package_name, total_cost, duration, weather, image) 
            VALUES ('$package_name', $total_cost, '$duration', '$weather', '$image')";

            if ($conn->query($insertQuery) === TRUE) {

                echo '
                    <div class="alert alert-success" role="alert">
                        Place Added Successful!
                    </div>';
            } else {
                echo "Error: " . $insertQuery . "<br>" . $conn->error;
            }

        } else {
            echo "Sorry, there was an error uploading your file.";
        }
    } else {
        echo "No file uploaded or there was an error with the upload.";
    }
}

